'use strict';

const floored = Math.floor(1.9);
console.log('Floored: ' + floored);

const ceiled = Math.ceil(1.1);
console.log('Ceiled: ' + ceiled);

const rounded = Math.ceil(1.5);
console.log('Rounded: ' + rounded);

const max = Math.max(5, 9, 15, 1, 3, 12);
console.log('Max: ' + max);

const min = Math.min(5, 9, 15, 1, 3, 12, -50);
console.log('Min: ' + min);

const powered = Math.pow(2, 16);
console.log('Powered: ' + powered);

const random = Math.random() * (10 - 1) + 1;
console.log('Random: ' + random);

const sqrt = Math.sqrt(12);
console.log('Squared root: ' + sqrt);

console.log('PI: ' + Math.PI);

const res = 100 % 13;
console.log('Res: ' + res);
